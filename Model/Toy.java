package Model;

public class Toy {

    private int idToy;
    private String nameToy;
    private int amountToys;
    private int weightToy;

    public int getIdToy() {
        return idToy;
    }

    public void setIdToy(int idToy) {
        this.idToy = idToy;
    }

    public String getNameToy() {
        return nameToy;
    }

    public void setNameToy(String nameToy) {
        this.nameToy = nameToy;
    }

    public int getAmountToys() {
        return amountToys;
    }

    public void setAmountToys(int amountToys) {
        this.amountToys = amountToys;
    }

    public int getWeightToy() {
        return weightToy;
    }

    public void setWeightToy(int weightToy) {
        this.weightToy = weightToy;
    }

}